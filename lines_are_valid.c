int	lines_are_valid(char **args)
{
	int	x;

	x = 0;
	while (x < 4)
	{
		if (args[2][x] == '4' && args[3][x] >= '2')
			return (0);
		if ((args[2][x] == '3' || args[2][x] == '2') && args[3][x] == '4')
			return (0);
		if (args[2][x] == '1' && args[3][x] == '1')
			return (0);
		if (args[2][x] == '3' && args[3][x] == '3')
			return (0);
		x++;
	}
	return (1);
}
